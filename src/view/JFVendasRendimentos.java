    private String formatarParaReal(double valor) {
        DecimalFormat df = new DecimalFormat("#,##0.00");
        return "R$ " + df.format(valor);
    }

    private void rendimento(ActionEvent evt) throws ParseException {
        ControllerProduto produto = new ControllerProduto();
        ArrayList<Produto> pproduto = produto.vendasRendimentos(evt);
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("Valo investido");
        table.addColumn("Valor com Lucro");
        table.addColumn("Total de Lucro");
        table.addColumn("Data da compra");
        double totalEntrada = 0;
        double totalSaida = 0;
        double lucro = 0;

        for (Produto l : pproduto) {

            totalEntrada += l.getQuantidade() * l.getValorEntrada();
            totalSaida += l.getQuantidade() * l.getValorS();
            lucro = totalSaida - totalEntrada;
            table.addRow(new Object[]{
                formatarParaReal(totalEntrada),
                formatarParaReal(totalSaida),
                formatarParaReal(lucro),
                l.getHorario()
            });

        }
        jTable1.setModel(table);
    }

