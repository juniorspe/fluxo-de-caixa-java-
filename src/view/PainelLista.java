    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Produto produto = new Produto();
        ControllerProduto cp = new ControllerProduto();
        try {
            
            produto.setNome(jTNome.getText());
            produto.setValorEntrada(Double.parseDouble(jTvalorDeCompra.getText().replace(",", ".")));
            produto.setQuantidade(Integer.parseInt(jTQuantidade.getText()));
            produto.setMargenDeLucro(Integer.parseInt(jTMargen.getText()));
            produto.setCodigo(jTCodigo.getText());
            boolean res = cp.update(produto);
            int linha = tbListaEditar.getSelectedRow();
            
            if (res == true) {
                tbListaEditar.setValueAt("Atualizado co sucesso!", linha, 4);
                jTNome.setText("");
                jTvalorDeCompra.setText("");
                jTQuantidade.setText("");
                jTMargen.setText("");
                jTCodigo.setText("");
                jTcold.setText("");
                atualizarTabela();
            }
            
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "deu muito errado" + e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

