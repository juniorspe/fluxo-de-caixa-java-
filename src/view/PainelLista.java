        private void atualizarTabela() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nome");
        modelo.addColumn("Quantidade");
        modelo.addColumn("Codigo");
        modelo.addColumn("Valor");
        modelo.addColumn("Margem de Lucro");
        for (Produto produto : produtos) {
            double valor = produto.getValorS();
            modelo.addRow(new Object[]{
                produto.getNome(),
                produto.getQuantidadeBanco(),
                produto.getCodigo(),
                formatarParaReal(valor)
            });
        }
        tbListaEditar.setModel(modelo);
        
    }
    

    private void jBListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBListarActionPerformed
        ControllerProduto controllerProduto = new ControllerProduto();
        DefaultTableModel llista = new DefaultTableModel();
        try {
            List<Produto> list = controllerProduto.listarProdutos(evt);
            llista.addColumn("Nome");
            llista.addColumn("Quantidade");
            llista.addColumn("Codigo");
            llista.addColumn("Valor");
            
            for (Produto l : list) {
                llista.addRow(new Object[]{
                    l.getNome(),
                    l.getQuantidadeBanco(),
                    l.getCodigo(),
                    formatarParaReal(l.getValorS())
                });
            }
            tbListaEditar.setModel(llista);
            //atualizarTabela();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        

    }//GEN-LAST:event_jBListarActionPerformed

