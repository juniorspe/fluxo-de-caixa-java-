    private void buscar() {
        ControllerProduto ctP = new ControllerProduto();
        
        try {
            
            Produto produto = ctP.bucarProduto(jTcold.getText());
            produtos.add(produto);
            atualizarTabela();
            produtos.clear();
            
        } catch (SQLException ex) {
            Logger.getLogger(PainelLista.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
