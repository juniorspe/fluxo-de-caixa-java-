    private void update() {
        
        int linha = tbListaEditar.getSelectedRow();
        if (linha != -1) {
            jTNome.setText((String) tbListaEditar.getValueAt(linha, 0));
            jTCodigo.setText((String) tbListaEditar.getValueAt(linha, 2));
        }
        
    }
