    private void desconto(){
        Produto ppp = new Produto();
        String desconto = JOptionPane.
                showInputDialog(this,
                "Quantos de desconto em %");
        double descontodouble = Double.parseDouble(desconto);
        double valors = 0;
         for (Produto produto:produtos){
            valors = produto.getValorS();
            produto.setValorS(valors - descontodouble * valors / 100);
             
         }
        
         atualizarTabela();
         atualizarValorTotal();
    
        
    }
    private void atualizarTabela() {
        Produto p = new Produto();
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Nome");
        modelo.addColumn("Codigo");
        modelo.addColumn("Quantidade");
        modelo.addColumn("Valor");

        for (Produto produto : produtos) {

            double valor = produto.mutiplicar(produto.getQuantidade());
            modelo.addRow(new Object[]{
                produto.getNome(),
                produto.getCodigo(),
                produto.getQuantidade(),
                formatarParaReal(valor)
            });

        }
        txAM.setModel(modelo);

    }

    private void atualizarValorTotal() {
        Produto pro = new Produto();
        double soma = 0;
        for (Produto produto : produtos) {
            double valor = produto.mutiplicar(produto.getQuantidade());
            soma += valor;
        }
        pro.setTotal(soma);
        JLvalor.setText(formatarParaReal(soma));

    }

    private String formatarParaReal(double valor) {
        DecimalFormat df = new DecimalFormat("#,##0.00");
        return "R$ " + df.format(valor);
    }

    private void EditaQuantidade() {
        int selection = txAM.getSelectedRow();
        String numero = JOptionPane.showInputDialog("Digite a quantidade!");
        int quantidade = Integer.parseInt(numero);
        Produto produto = produtos.get(selection);
        produto.setQuantidade(quantidade);
        atualizarTabela();
        atualizarValorTotal();
    }

