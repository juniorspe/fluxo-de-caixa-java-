    private void funcaoTroco() {
        String valorMaior = JOptionPane.showInputDialog("Digite o valor!");
        String valor = JLvalor.getText();
        String novovalor = "";
        for (String valores : valor.split("[^0-9]")) {
            novovalor += valores;
        }
        double valorSubiraindo = Double.parseDouble(
                valorMaior) - Double.parseDouble(
                        novovalor) / 100;
        jLTroco.setText(formatarParaReal(valorSubiraindo));
    }
