    private void cadastraVenda() throws Exception {
        ControllerProduto cp = new ControllerProduto();
        Produto produto = new Produto();
        String data = "dd/MM/YYYY H:mm - a";
        String data1;
        SimpleDateFormat formato = new SimpleDateFormat(data);
        Date tmpoAtual = new Date();
        data1 = formato.format(tmpoAtual);

        ArrayList<Produto> ppproduto = new ArrayList();

        for (Produto pproduto : produtos) {
            produto.setNome(pproduto.getNome());
            produto.setCodigo(pproduto.getCodigo());
            produto.setValorEntrada(pproduto.getValorEntrada());
            produto.setValorS(pproduto.calcularValor());
            produto.setQuantidade(pproduto.getQuantidade());
            pproduto.setTotal(pproduto.mutiplicar(pproduto.getQuantidade()));
            pproduto.setHorario(data1);
            ppproduto.add(pproduto);

        }

        cp.cadastrarVenda((ArrayList<Produto>) ppproduto);

        produtos.clear();
        atualizarValorTotal();
        atualizarTabela();

    }
