    private void atualizaHoras() {
        TimerTask task = new TimerTask() {
            @Override
            public void run() {
                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/YYYY H:mm:ss - a");
                String dataFormatada = formato.format(new Date());
                jLabel5.setText(dataFormatada);
            }
        };
        Timer timer = new Timer();
        timer.schedule(task, new Date(), 1000);
    }
    private void PegarDados() {
        String codigo = jTcodigo.getText();
        if (codigo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo vazio!");
            return;
        }
        boolean produtoJaExiste = false;
        for (Produto produto : produtos) {
            if (produto.getCodigo().equals(codigo)) {
                produto.setQuantidade(produto.getQuantidade() + 1);
                produtoJaExiste = true;
            }
        }
        if (produtoJaExiste) {
            jTcodigo.setText("");
            atualizarValorTotal();
            atualizarTabela();
            return;
        }
        ControllerProduto cp = new ControllerProduto();
        try {
            Produto produto = cp.bucarProduto(codigo);
            if (produto == null) {
                JOptionPane.showMessageDialog(null,
                        "Produto nao encontrado!!");
                return;
            }
            produto.setQuantidade(1);
            produtos.add(produto);
            jTcodigo.setText("");
            atualizarValorTotal();
            atualizarTabela();
        } catch (SQLException ex) {
            Logger.getLogger(PainelVendas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

